/**
 * Card Component with Tailwind CSS
 * Modern card design with dark mode support
 */

import { HTMLAttributes, forwardRef } from 'react';
import { cn } from '@/lib/utils';

export interface CardProps extends HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'bordered' | 'featured';
  padding?: 'sm' | 'md' | 'lg' | 'none';
  hoverable?: boolean;
}

export const Card = forwardRef<HTMLDivElement, CardProps>(
  (
    {
      className,
      variant = 'default',
      padding = 'md',
      hoverable = false,
      children,
      ...props
    },
    ref
  ) => {
    const baseStyles = 'rounded-xl transition-all duration-200';

    const variants = {
      default:
        'bg-white dark:bg-gray-800 shadow-md border border-gray-100 dark:border-gray-700',
      bordered:
        'bg-white dark:bg-gray-800 border-2 border-gray-200 dark:border-gray-700',
      featured:
        'bg-gradient-to-br from-teal-50 to-indigo-50 dark:from-gray-800 dark:to-gray-900 border border-teal-200 dark:border-indigo-800 shadow-lg',
    };

    const paddings = {
      none: '',
      sm: 'p-4',
      md: 'p-6',
      lg: 'p-8',
    };

    const hoverStyles = hoverable
      ? 'cursor-pointer hover:shadow-xl hover:-translate-y-1'
      : '';

    return (
      <div
        ref={ref}
        className={cn(
          baseStyles,
          variants[variant],
          paddings[padding],
          hoverStyles,
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

